        let animationFrameId;

        const onLoadImage = () => {
            const canvas = canvasRef.current;
            const ctx = canvas.getContext('2d');

            canvas.width = 366;
            canvas.height = 473;

            ctx.drawImage(myImage, 0, 0, canvas.width, canvas.height);

            const pixels = ctx.getImageData(0, 0, canvas.width, canvas.height);
            ctx.clearRect(0, 0, canvas.width, canvas.height);


            let mappedImage = [];
            for(let y = 0; y < canvas.height; y++) {
                let row = [];
                for(let x = 0; x < canvas.width; x++) {
                   const red = pixels.data[(y * 4 * pixels.width) + (x * 4)];
                   const green = pixels.data[(y * 4 * pixels.width) + (x * 4 + 1)];
                   const blue = pixels.data[(y * 4 * pixels.width) + (x * 4 + 2)];
                    const brightness = calculateRelativeBrightness(red, green, blue);
                    const cell = {
                        cellBrightness: brightness,
                        color: `rgb(${red}, ${green}, ${blue})`
                    }

                    row.push(cell);
                }
                mappedImage.push(row);
            }

            function calculateRelativeBrightness(red, green, blue) {
                return Math.sqrt(
                    (red * red) * 0.299 +
                    (green * green) * 0.587 +
                    (blue * blue) * 0.114
                )/100
            }

            function animate() {
                ctx.globalAlpha = 0.05;
                ctx.fillStyle = 'rgb(0, 0, 0)';
                ctx.fillRect(0, 0, canvas.width, canvas.height);
                ctx.globalAlpha = 0.2;
                for(let i = 0; i< particleArray.length; i++) {
                    particleArray[i].update(canvas, mappedImage);
                    ctx.globalAlpha = particleArray[i].speed * 0.01;
                    particleArray[i].draw(ctx, mappedImage);
                }
                animationFrameId = requestAnimationFrame(animate);
            }

            if(animationToggler){
                init(particlesAmount, radius, velocity, canvas.width);
                animate()
            };
        }

        myImage.addEventListener('load', onLoadImage);

        return () => {
            window.cancelAnimationFrame(animationFrameId);
            myImage.removeEventListener('load', onLoadImage)
        }
